package org.example.week1;

public class ShortHandOperatorsDemo {
    public static void main(String[] args) {

        int a = 50;
        a += 25;
        //a = a + 25
        System.out.println(a);


        a = 50;
        a -= 25;
        //a = a - 25
        System.out.println(a);


        a = 50;
        a *= 25;
        //a = a * 25
        System.out.println(a);

        a = 50;
        a /= 25;
        //a = a / 25
        System.out.println(a);
        a = 50;
        a %= 25;
        //a = a - 25
        System.out.println(a);
    }

}
